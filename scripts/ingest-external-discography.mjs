// POST every entry in a bulk-scrape JSON file to the admin import
// endpoint, in series with progress reporting. Resumable — entries
// already in --acked are skipped on re-run.
//
//   node scripts/ingest-external-discography.mjs \
//        --in=scratch/wirz-all.json \
//        --url=http://localhost:5000 \
//        --cookie='__session=...' \
//        [--acked=scratch/wirz-acked.json] [--delay=200]
//
// After the POSTs finish, hit the purge endpoint to drop any external
// rows already covered by release_cache:
//   curl -X POST $URL/api/admin/external-discography/purge-covered \
//     -H 'cookie: $COOKIE'
//
// The bulk-scrape output is an array of payloads, each shaped like
// the admin import endpoint expects (the file is generated by
// scrape-wirz-all.mjs / scrape-abrams-all.mjs).

import fs from "node:fs";
import path from "node:path";

const argv = Object.fromEntries(
  process.argv.slice(2).map(a => a.split("=", 2)).map(([k, v]) => [k.replace(/^--/, ""), v ?? true]),
);
const IN_PATH    = argv.in;
const URL_BASE   = argv.url;
const COOKIE     = argv.cookie;
const DELAY_MS   = Number(argv.delay ?? 200);
const ACKED_PATH = argv.acked ?? (IN_PATH ? IN_PATH.replace(/\.json$/, "-acked.json") : null);
if (!IN_PATH || !URL_BASE || !COOKIE) {
  console.error("usage: --in=<bulk.json> --url=<api-base> --cookie='__session=…' [--acked=<acked.json>] [--delay=200]");
  process.exit(1);
}

const payloads = JSON.parse(fs.readFileSync(IN_PATH, "utf8"));
let acked = [];
if (ACKED_PATH && fs.existsSync(ACKED_PATH)) {
  try { acked = JSON.parse(fs.readFileSync(ACKED_PATH, "utf8")); } catch {}
}
const ackedKeys = new Set(acked.map(a => a._key));

// Per-source endpoint URL — both wirz + abrams hit the same import
// endpoint.
const ENDPOINT = `${URL_BASE.replace(/\/$/, "")}/api/admin/external-discography/import`;

let ok = 0, failed = 0, skipped = 0;
for (let i = 0; i < payloads.length; i++) {
  const p = payloads[i];
  // Strip the internal _slug used by the resumable scrape runner so it
  // doesn't get rejected by the endpoint's strict body shape.
  const body = { ...p };
  delete body._slug;
  const key = `${p.source}|${p._slug ?? p.label}`;
  if (ackedKeys.has(key)) { skipped += 1; continue; }
  try {
    const res = await fetch(ENDPOINT, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Cookie":       COOKIE,
      },
      body: JSON.stringify(body),
    });
    if (res.ok) {
      const j = await res.json();
      ok += 1;
      acked.push({ _key: key, label: p.label, inserted: j.inserted });
      if (ACKED_PATH) fs.writeFileSync(ACKED_PATH, JSON.stringify(acked, null, 2));
      console.error(`[${i + 1}/${payloads.length}] ✓ ${p.label} — ${j.inserted} rows`);
    } else {
      failed += 1;
      console.error(`[${i + 1}/${payloads.length}] ✗ ${p.label} — HTTP ${res.status} ${await res.text().catch(() => "")}`);
    }
  } catch (err) {
    failed += 1;
    console.error(`[${i + 1}/${payloads.length}] ✗ ${p.label} — ${err.message}`);
  }
  await new Promise(r => setTimeout(r, DELAY_MS));
}

console.error(`done: ok=${ok}, failed=${failed}, skipped=${skipped}`);
